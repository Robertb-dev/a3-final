    private void btbCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbCadastrarActionPerformed
        // TODO add your handling code here:

        try {

            PacienteModel paciente = new PacienteModel();
            PacienteDAO pacienteDao = new PacienteDAO();

            paciente.setNome(txtNome.getText());
            paciente.setCpf(txtCpf.getText());
            paciente.setIdade(Integer.parseInt(txtIdade.getText()));
            paciente.setPeso(Double.parseDouble(txtPeso.getText()));
            paciente.setAltura(Integer.parseInt(txtAltura.getText()));
            paciente.setTmb(Double.parseDouble(txtTmb.getText()));

            pacienteDao.save(paciente);

        } catch (Exception e) {

            e.printStackTrace();

        }
        txtNome.setText("");
        txtCpf.setText("");
        txtPeso.setText("");
        txtIdade.setText("");
        txtAltura.setText("");
        txtTmb.setText("");

        dispose();
        setVisible(true);
        readJTable();


    }//GEN-LAST:event_btbCadastrarActionPerformed

    private void btnExcluir1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluir1ActionPerformed
        // TODO add your handling code here:

        if (jtPaciente.getSelectedRow() != -1) {

            try {

                PacienteModel paciente = new PacienteModel();
                PacienteDAO pacienteDao = new PacienteDAO();

                paciente.setId((int) jtPaciente.getValueAt(jtPaciente.getSelectedRow(), 0));

                pacienteDao.delete(paciente);

            } catch (Exception e) {

                e.printStackTrace();

            }
            txtNome.setText("");
            txtCpf.setText("");
            txtPeso.setText("");
            txtIdade.setText("");
            txtAltura.setText("");
            txtTmb.setText("");

            dispose();
            setVisible(true);
            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selcione um paciente para excluir");
        }


    }//GEN-LAST:event_btnExcluir1ActionPerformed

