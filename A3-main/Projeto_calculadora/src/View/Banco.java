    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jtPaciente.getModel();
        modelo.setNumRows(0);
        PacienteDAO pDao = new PacienteDAO();

        for (PacienteModel p : pDao.read()) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getCpf(),
                p.getIdade(),
                p.getPeso(),
                p.getAltura(),
                p.getTmb()
            });
        }
    }

